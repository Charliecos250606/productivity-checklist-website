const form = document.getElementById("form");
const category = Object.keys(questionnaire);
let currentIndex = 0;

function renderingIndex() {
  const categoryName = category[currentIndex];
  const h3 = document.createElement("h3");
  h3.textContent = categoryName.toUpperCase();
  form.appendChild(h3);

  for (let questions of questionnaire[categoryName]) {
    const p = document.createElement("p");
    p.className = "question";
    p.textContent = `${questions.id}. ${questions.question}`;
    form.appendChild(p);

    for (let j = 1; j <= 4; j++) {
      const label = document.createElement("label");
      label.classList = "option";
      const input = document.createElement("input");
      input.type = "radio";
      input.value = j;
      input.name = `${questionnaire[categoryName]}_${questions.id}`;
      label.appendChild(input);
      label.append(`${questions.options[j - 1]}`);
      form.appendChild(label);
    }
  }
  if (currentIndex === 0) {
    prevBtn.style.display = "none";
  } else {
    prevBtn.style.display = "inline-block";
  }
  if (currentIndex === category.length - 1) {
    nextBtn.style.display = "none";
    calculateBtn.style.display = "inline-block";
  } else {
    nextBtn.style.display = "inline-block";
    calculateBtn.style.display = "none";
  }
  const progressRead = document.querySelector(".progressRead");
  const progressValue = document.querySelector(".progressValue");
  progressRead.style.width = currentIndex * (100 / (category.length - 1)) + "%";
  console.log(currentIndex * (100 / (category.length - 1)) + "%");
  progressValue.textContent =
    currentIndex * (100 / (category.length - 1)) + "%";
}

prevBtn.addEventListener("click", moveToPrev);
nextBtn.addEventListener("click", moveToNext);

renderingIndex();

function moveToNext() {
  if (currentIndex < category.length) {
    form.innerHTML = "";
    currentIndex = currentIndex + 1;
    renderingIndex();
  }
}
function moveToPrev() {
  if (currentIndex > 0) {
    form.innerHTML = "";
    currentIndex = currentIndex - 1;
    renderingIndex();
  }
}

